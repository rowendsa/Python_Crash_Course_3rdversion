#This program is an illustration for sorting the list permanent in reverse
cars = ['bmw', 'audi', 'toyota', 'subaru']
print("Original Order")
print(cars)

#permanently sorting the list
cars.sort()
print("After Ordering")
print(cars)

cars.sort(reverse=True)
print("After Reverse Ordering")
print(cars)